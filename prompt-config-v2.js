export function buildPromptV2({spu, contentGroup, contentAngle, format, persona, goal, pain, selling}) {
  return [
    systemPrompt,
    spuPromptMap[spu] || "",
    contentGroupPromptMap[spu]?.[contentGroup] || "",
    contentAnglePromptMap[spu]?.[contentAngle] || "",
    formatPromptMap[format] || "",
    personaPromptMap[persona] || "",
    goalPromptMap[goal] || "",
    `【用户输入】\n核心问题：${pain}\n核心卖点：${selling || ""}`
  ].filter(Boolean).join("\n\n");
}

export function getJsonSchema(format) {
  if (format === "video") {
    return {
      type: "object",
      additionalProperties: false,
      properties: {
        titles: { type: "array", items: { type: "string" }, minItems: 3, maxItems: 3 },
        hook: { type: "string" },
        script: { type: "string" },
        commentCTA: { type: "string" },
        usageNote: { type: "string" }
      },
      required: ["titles", "hook", "script", "commentCTA", "usageNote"]
    };
  }
  return {
    type: "object",
    additionalProperties: false,
    properties: {
      titles: { type: "array", items: { type: "string" }, minItems: 3, maxItems: 3 },
      coverText: { type: "string" },
      body: { type: "string" },
      commentCTA: { type: "string" },
      usageNote: { type: "string" }
    },
    required: ["titles", "coverText", "body", "commentCTA", "usageNote"]
  };
}
